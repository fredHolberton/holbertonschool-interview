#ifndef HOLBERTON_H
#define HOLBERTON_H

/**
 * wildcmp - Compares two strings, supporting '*' as a wildcard
 * @s1: First string (target)
 * @s2: Second string (pattern, may contain '*')
 *
 * Return: 1 if strings match (with wildcard support), 0 otherwise
 */
int wildcmp(char *s1, char *s2);

#endif /* HOLBERTON_H */
